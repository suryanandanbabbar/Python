# This code demonstrates math Module
import math
num = float(input("num: "))


print("Ceil:",math.ceil(num))
print("Trunc:", math.trunc(num))
print("Floor:", math.floor(num))
print("GCD:", math.gcd(int(num)))
print("Factorial:", math.factorial(int(num)))
print("Exp:", math.exp(num))
print("log:", math.log(num))
print("power:", math.pow(num, 2))
print("sqrt:", math.sqrt(num))
print("sin:", math.sin(num))
print("cos:", math.cos(num))
print("tan:", math.tan(num))
print("degrees:", math.degrees(num))
print("radians:", math.radians(num))
